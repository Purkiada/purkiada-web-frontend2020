<p>Barevný model <strong>RGB</strong> neboli červená (red) – zelená (green) – modrá (blue) je aditivní způsob míchání barev používaný ve všech monitorech a projektorech.</p>
<p><strong>CMYK</strong> je barevný model založený na subtraktivním míchání barev. CMYK se používá především u reprodukčních zařízení, která barvy tvoří mícháním pigmentů (např. inkoustová tiskárna). Model obsahuje čtyři základní barvy:
<ul>
	<li>Azurová (cyan) </li>
	<li>Purpurová (magenta) </li>
	<li>Žlutá (yellow) </li>
	<li>Černá (key) </li>
</ul>   
</p>
<p>Barevný model <strong>HSV</strong> nejvíce odpovídá lidskému vnímání barev. Je složen z:
<ul>
  <li>Hue – odstín</li>
	<li>Saturation – sytost</li>
	<li>Value - jas</li>
</ul>
</p>
